    private void mtdlistar(){
            modelo.setRowCount(0);
            
            clsNArticulo objNa = new clsNArticulo();
            ResultSet rs = objNa.mtdListarArticulo();  // Corrección aquí
            try {
                while (rs.next()) {
                    String dato[] = new String[6];
                    dato[0] = rs.getString("id_articulo");
                    dato[1] = rs.getString("id_empleado");
                    dato[2] = rs.getString("nombre");
                    dato[3] = rs.getString("descripcion");
                    dato[4] = rs.getString("fecha");
                    dato[5] = rs.getString("precio_unitario");
                    modelo.addRow(dato);
                }
            } catch (SQLException ex) {
                // Aquí puedes agregar un manejo de la excepción, como imprimir el error
                ex.printStackTrace();
            }
            tblArticulo.setModel(modelo); 
    }
