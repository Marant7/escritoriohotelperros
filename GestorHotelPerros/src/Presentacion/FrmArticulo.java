    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        clsEArticulo objEA = new clsEArticulo();
        objEA.setId_articulo(Integer.parseInt(txtBuscar.getText()));
        clsNArticulo objNA = new clsNArticulo();
        ResultSet rs = objNA.mtdBuscarArticulo(objEA);

        // Limpiar el modelo de la tabla antes de agregar nuevos datos
        DefaultTableModel modelo = (DefaultTableModel) tblArticulo.getModel();
        modelo.setRowCount(0); // Elimina todas las filas existentes

        try {
            while (rs.next()) {
                String dato[] = new String[6]; // Ajustar al número de columnas que deseas mostrar
                dato[0] = rs.getString("id_articulo");
                dato[1] = rs.getString("id_empleado");
                dato[2] = rs.getString("nombre");
                dato[3] = rs.getString("descripcion");
                dato[4] = rs.getString("fecha");
                dato[5] = rs.getString("precio_unitario");
                modelo.addRow(dato);
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // Manejo básico de excepciones, imprime el error en consola
        }

        tblArticulo.setModel(modelo); // Establecer el modelo actualizado en la tabla
    }//GEN-LAST:event_btnBuscarActionPerformed

