    private void listarArticulos(){
        mtdLimpiarTabla(modeloArticulo);
        clsNDetalleServicio objNDS = new clsNDetalleServicio();
        ResultSet rs = objNDS.mtdListaArticulos();
        try {
            while (rs.next()) {
                String dato[] = new String[4];
                dato[0] = rs.getString("id_articulo");
                dato[1] = rs.getString("nombre");
                dato[2] = rs.getString("descripcion");
                dato[3] = rs.getString("precio_unitario");
                modeloArticulo.addRow(dato);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        tblArticulo.setModel(modeloArticulo);
    }
    
    private void llenarComboBox() {
        clsNDetalleServicio objNDS = new clsNDetalleServicio();
        ResultSet rs = objNDS.obtenerServicios();
        try {
            cmbServicio.removeAllItems(); // Limpiar items previos
            mapServicios.clear(); // Limpiar mapa previo
            while (rs.next()) {
                int idServicio = rs.getInt("id_servicio");
                String descripcion = rs.getString("descripcion");
                cmbServicio.addItem(descripcion);
                mapServicios.put(idServicio, descripcion);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void listarDetalleServicio(){
        mtdLimpiarTabla(modeloDetalleServicio);
        clsNDetalleServicio objNE = new clsNDetalleServicio();
        ResultSet rs = objNE.mtdListarDetalleServicio();
        try {
            while (rs.next()) {
                String dato[] = new String[5];
                dato[0] = rs.getString("id_detalle_servicio");
                dato[1] = rs.getString("id_servicio");
                dato[2] = rs.getString("id_articulo");
                dato[3] = rs.getString("cantidad");
                dato[4] = rs.getString("descripcion");
                modeloDetalleServicio.addRow(dato);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        tblDetalleServicio.setModel(modeloDetalleServicio);
    }
