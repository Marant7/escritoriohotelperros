    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    clsEDetalleServicio objEDS = new clsEDetalleServicio();
    clsNDetalleServicio objNDS = new clsNDetalleServicio();

    // Obtener los valores de los campos de texto
    int idDetalleServicio = Integer.parseInt(txtIdDetalleServicio.getText());
    int idServicio = Integer.parseInt(txtIdServicio.getText());
    int idArticulo = Integer.parseInt(txtIdArticulo.getText());

    // Verificar que el campo de cantidad (txtCantidad) no esté vacío antes de convertirlo a entero
    String cantidadTexto = txtCantidad.getText().trim();
    if (cantidadTexto.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, ingresa una cantidad válida.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
        return; // Salir del método si el campo está vacío
    }

    // Verificar que el valor ingresado sea un número entero positivo o cero
    int cantidadUso;
    try {
        cantidadUso = Integer.parseInt(cantidadTexto);
        if (cantidadUso < 0) {
            JOptionPane.showMessageDialog(null, "Por favor, ingresa una cantidad mayor o igual a 0.", "Valor Inválido", JOptionPane.WARNING_MESSAGE);
            return; // Salir del método si el valor es negativo
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, ingresa un número entero válido.", "Valor Inválido", JOptionPane.WARNING_MESSAGE);
        return; // Salir del método si no se pudo convertir a entero
    }

    // Setear los atributos en el objeto clsEDetalleServicio
    objEDS.setId_detalle_servicio(idDetalleServicio);
    objEDS.setId_servicio(idServicio);
    objEDS.setId_articulo(idArticulo);
    objEDS.setCantidad(cantidadUso);

    // Llamar al método de edición y mostrar mensaje según el resultado
    if (objNDS.mtdEditarDetalleServicio(objEDS)) {
        JOptionPane.showMessageDialog(null, "Se editó el detalle de servicio correctamente");
        listarDetalleServicio();  // Actualizar la tabla después de editar
    } else {
        JOptionPane.showMessageDialog(null, "Error al guardar el detalle de servicio");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

