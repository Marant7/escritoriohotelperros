    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        clsEServicio objAr = new clsEServicio();
        clsNServicio objNAr = new clsNServicio();

        // Validar que txtCodigo no esté vacío
        String codigo = txtIdServicio.getText().trim();
        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione un código.");
            return;
        }

        // Validar que txtNombre no esté vacío
        String nombre = txtDescripcion.getText().trim();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese una descripcion.");
            return;
        }

        // Validar que txtPrecio contenga un número mayor que cero
        String precioText = txtPrecio.getText().trim();
        if (!precioText.matches("^\\d*\\.?\\d+$") || Float.parseFloat(precioText) <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un precio válido mayor que cero.");
            return;
        }

        // Setear atributos del artículo
        objAr.setId_servicio(Integer.parseInt(txtIdServicio.getText()));
        objAr.setDescripcion(txtDescripcion.getText());
        objAr.setCantidad(Float.parseFloat(txtPrecio.getText()));

        if (objNAr.mtdActualizarServicio(objAr)) {
            JOptionPane.showMessageDialog(null, "DATOS ACTUALIZADOS");
             mtdlistar();
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar el artículo");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

