    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        clsEServicio objEA = new clsEServicio();
        objEA.setId_servicio(Integer.parseInt(txtBuscar.getText()));
        clsNServicio objNA = new clsNServicio();
        ResultSet rs = objNA.mtdBuscarArticulo(objEA);

        // Limpiar el modelo de la tabla antes de agregar nuevos datos
        DefaultTableModel modelo = (DefaultTableModel) tblArticulo.getModel();
        modelo.setRowCount(0); // Elimina todas las filas existentes

        try {
            while (rs.next()) {
                String dato[] = new String[3]; // Ajustar al número de columnas que deseas mostrar
                    dato[0] = rs.getString("id_servicio");
                    dato[1] = rs.getString("descripcion");
                    dato[2] = rs.getString("precio");
                modelo.addRow(dato);
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // Manejo básico de excepciones, imprime el error en consola
        }

        tblArticulo.setModel(modelo); // Establecer el modelo actualizado en la tabla
    }//GEN-LAST:event_btnBuscarActionPerformed
    private void mtdlistar(){
            modelo.setRowCount(0);
            
            clsNServicio objNa = new clsNServicio();
            ResultSet rs = objNa.mtdListarArticulo();  // Corrección aquí
            try {
                while (rs.next()) {
                    String dato[] = new String[3];
                    dato[0] = rs.getString("id_servicio");
                    dato[1] = rs.getString("descripcion");
                    dato[2] = rs.getString("precio");
                    modelo.addRow(dato);
                }
            } catch (SQLException ex) {
                // Aquí puedes agregar un manejo de la excepción, como imprimir el error
                ex.printStackTrace();
            }
            tblArticulo.setModel(modelo); 
    }
